package org.zywx.wbpalmstar.widgetone.uex11597450.data;

public class RandomData {
    private int times;//总次数
    private int incrementTimes;//每天自增次数
    private String courseId;//标识
    private String date;//日期

    public int getTimes() {
        return times;
    }

    public void setTimes(int times) {
        this.times = times;
    }

    public int getIncrementTimes() {
        return incrementTimes;
    }

    public void setIncrementTimes(int incrementTimes) {
        this.incrementTimes = incrementTimes;
    }

    public String getCourseId() {
        return courseId;
    }

    public void setCourseId(String courseId) {
        this.courseId = courseId;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
